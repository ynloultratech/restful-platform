namespace Ynlo\RestfulPlatformBundle\Swagger\Specification\Schema;

trait TypeAwareTrait
{
    /**
     * @var string
     */
    protected $type;

    /**
     * @see https://swagger.io/specification/#dataTypeFormat
     *
     * @var string
     */
    protected $format;

    /**
     * @return string
     */
    public function getType()
    {
        return $this->type;
    }

    /**
     * @param string $type
     */
    public function setType(string $type = null)
    {
        $this->type = $type;
    }

    /**
     * @return string
     */
    public function getFormat()
    {
        return $this->format;
    }

    /**
     * @param string $format
     */
    public function setFormat(string $format = null)
    {
        $this->format = $format;
    }
}